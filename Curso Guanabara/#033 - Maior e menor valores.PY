primeiro_valor = int(input('Digite o primerio valor: '))
segundo_valor = int(input('Digite o segundo valor: '))
terceiro_valor = int(input('Digite o terceiro valor: '))


print(f'O maior valor digitado foi {max(primeiro_valor, segundo_valor, terceiro_valor)}')
print(f'O menor valor digitado foi {min(primeiro_valor, segundo_valor, terceiro_valor)}')